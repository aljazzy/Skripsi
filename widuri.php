<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem manusia dan Sistem mesin. Sistem manusia dan sistem mesin merupakan sebuah klasifikasi sistem jika dipandang dari pelakunya. Pada zaman yang semakin global dan semuanya serba maju ini tidak semua sistem dikerjakan oleh manusia tapi beberapa sistem dikerjakan oleh mesin tergantung dari kebutuhannya. Sistem manusia adalah suatu sistem yang proses kerjanya dilakukan oleh manusia sebagai contoh pelaku sistem organisasi, sistem akademik yang masih manual, transaksi jual beli di pasar tradisional, dll. Adapun sistem mesin merupakan sebuah sistem yang proses kerjanya dilakukan oleh mesin, sebagai contoh sistem motor, mobil, mesin industri, dan lain-lain.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem sederhana dan Sistem kompleks. Sistem dilihat dari tingkat kekomplekan masalahnya dibagi menjadi dua yaitu sistem sederhana dan sistem kompleks. Sistem sederhana merupakan sistem yang sedikit subsistemnya dan komponen-komponennya pun sedikit. Adapun sistem kompleks adalah sistem yang banyak sub-sub sistemnya sehingga proses dari sistem itu sangat rumit.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem bisa beradaptasi dan Sistem tidak bisa beradaptasi. Sistem yang bisa berdaptasi terhadap lingkungannya merupakan sebuah sistem yang mampu bertahan dengan adanya perubahan lingkungan. Sedangkan sistem yang tidak bisa beradaptasi dengan lingkungan merupakan sebuah sistem yang tidak mampu bertahan jika terjadi perubahan lingkungan.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem Alamiah (Natural System) dan Sistem Buatan Manusia (Human Made System). Sistem alamiah adalah sistem yang terjadi melalui proses alam, tidak dibuat manusia. Misalnya sistem tata surya. Sistem buatan manusia adalah sistem yang melibatkan interaksi manusia dengan mesin yang disebut human machine system. Misalnya sistem telekomunikasi.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem sementara dan sistem selamanya. Sistem sementara dan sistem selamanya merupakan klasifikasi sistem jika dilihat dari pemakaiannya. Sistem sementara merupakan sebuah sistem yang dibangun dan digunakan untuk waktu sementara waktu sebagai contoh sistem pemilihan presiden, setelah proses pemilihan presiden sudah tidak dipakai lagi dan untuk pemilihan lima tahun mendatang kemungkinan sudah dibuat sistem pemilihan presiden yang baru. Sedangkan sistem selamanya merupakan sistem yang dipakai untuk jangka panjang atau digunakan selamanya, misalnya sistem pencernaan.</p></li>
</ol>

====Tujuan Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Adapun tujuan sistem menurut Azhar Susanto (2013:23)<ref name="susanto"/>, adalah target atau sasaran akhir yang ingin dicapai oleh suatu sistem. Agar  target tersebut bisa tercapai, maka target atau sasaran tersebut harus diketahui terlebih dahulu ciri-ciri atau kriterianya. Upaya mencapai suatu sasaran tanpa mengetahui ciri-ciri atau kriteria dari sasaran tersebut kemungkinan besar sasaran tersebut tidak akan pernah tercapai. Ciri-ciri atau kriteria dapat juga digunakan sebagai tolak ukur dalam menilai suatu keberhasilan suatu sistem dan menjadi dasar dilakukannya suatu pengendalian. </p></div>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Dari pengertian diatas dapat ditarik kesimpulan bahwa sistem merupakan kumpulan suatu komponen sistem yang saling berhubungan satu dengan yang lain untuk mencapai tujuan suatu kegiatan pokok perusahaan.</p></div>

===Konsep Dasar Informasi===
====Definisi Data====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berikut ini terdapat pendapat para ahli mengenai definisi data, yaitu:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Taufiq (2013:13)<ref name="taufiq"/>, Data adalah sesuatu yang diberikan untuk kemudian diolah.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Thompson & Handelman dalam buku Bambang Hartono (2013:15)<ref name="hartono">Hartono, Bambang. 2013. Sistem Informasi Manajemen Berbasis Komputer. Rineka Cipta: Jakarta.</ref>, Data adalah hasil pengukuran dan pencatatan terhadap fakta tentang sesuatu, keadaan, tindakan atau kejadian.</p></li>
</ol>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berdasarkan definisi diatas, maka dapat disimpulkan bahwa data merupakan gambaran dari suatu kejadian, data terdiri dari suatu fakta dan angka yang kemudian diolah untuk menghasilkan suatu informasi.</p></div>

====Definisi Informasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Informasi sangat penting dan diperlukan didalam suatu sistem. Berikut ini adalah beberapa definisi informasi, yaitu:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Terry dalam Yakub (2014:17)<ref name="yakubvico">Yakub. Hisbanarto, Vico. 2014. Sistem Informasi Manajemen Pendidikan. Yogyakarta: Graha Ilmu.</ref>, Informasi adalah data penting dan memberikan pengetahuan yang berguna. Informasi merupakan pengumpulan dan pengolahan data untuk memberikan keterangan atau pengetahuan.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Pratama (2014:9)<ref name="pratama">Pratama, I Putu Agus Eka. 2014. Sistem Informasi dan Implementasinya. BI Obses: Bandung.
</ref>, Informasi merupakan hasil pengolahan data dari satu atau berbagai sumber, yang kemudian diolah, sehingga memberikan nilai, arti, dan manfaat.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Menurut Yulia Djahir dan Dewi Pratita (2014: 10)<ref name="yulia">Djahir, Yulia dan Pratita, Dewi. 2014. Bahan Ajar Sistem Informasi Manajemen. Yogyakarta: Deepublish.</ref>, Informasi adalah hasil dari pengolahan data menjadi bentuk yang lebih berguna bagi yang menerimanya yang menggambarkan suatu kejadian-kejadian nyata dan dapat digunakan sebagai alat bantu untuk pengambilan suatu keputusan.</p></li>
</ol>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Dari beberapa pendapat diatas, dapat disimpulkan bahwa informasi merupakan data penting yang telah diolah yang didapatkan dari berbagai sumber sehingga memberikan nilai, arti, dan manfaat serta membantu dalam proses pengambilan keputusan.</p></div>

====Kualitas Informasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berikut ini disampaikan delapan kriteria yang dapat digunakan untuk menentukan nilai dari suatu informasi. Penjelasan tentang kualitas informasi tersebut dipaparkan di bawah ini menurut Lippeveld, Sauborn, dan Bodart di dalam bukunya Bambang Hartono (2013:17-18)<ref name="hartono"/>:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Relevansi</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi disediakan atau disajikan untuk digunakan. Oleh karena itu, informasi yang bernilai tinggi adalah yang relevan dengan kebutuhan, yaitu untuk apa informasi itu akan digunakan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kelengkapan dan Keluasan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi akan bernilai semakin tinggi, jika tersaji secara lengkap dalam cakupan yang luas. Informasi yang sepotong-sepotong, apalagi tidak tersusun sistematis, tentu tidak akan banyak artinya. Demikian pun bila informasi itu hanya mencangkup area yang sempit dari suatu permasalahan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kebenaran</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Kebenaran informasi ditentukan oleh validitas atau dapatnya dibuktikan. Informmasi berasal dari data, dan data fakta. Informasi yang bernilai tinggi adalah informasi yang benar-benar berasal dari fakta, bukan opini atau ilusi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Terukur</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi berasal dari data arau hasil pengukuran dan pencacatan terhadap fakta. Jadi, informasi yang bernilai tinggi adalah informasi yang jika dilacak kembali kepada datanya, data tersebut dapat diukur sesuai dengan faktanya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Keakuratan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi berasal dari data arau hasil pengukuran dan pencacatan terhadap fakta. Oleh karena itu kecermatan dalam mengukur dan mencatat fakta akan menentukan keakuratan data dan nilai dari informasi yang dihasilkan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kejelasan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi dapat disajikan dalam berbagai bentuk teks, tabel, grafik, chart, dan lain-lain. Namun, apa pun bentuk yang dipilih, yang penting adalah menjadikan pemakai mudah memahami maknanya. Oleh sebab itu, selain bentuk penyajiannya harus benar, juga harus diperhatikan kemampuan pemakai dalam memahaminya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Keluwesan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi yang baik adalah yang mudah diubah-ubah bentuk penyajiannya sesuai dengan kebutuhan dan situasi yang dihadapi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Ketepatan Waktu</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi yang baik adalah informasi yang disajikan tepat pada saat dibutuhkan. Informasi yang terlambat datang menjadi informasi basi yang tidak ada lagi nilainya.</p>
</ol>

====Nilai Informasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Davis dalam Sutarman (2012:14)<ref name="sutarman">Sutarman. 2012. Buku Pengantar Teknologi Informasi. Jakarta: Bumi Aksara.</ref> menjelaskan, Nilai informasi dikatakan sempurna apabila perbedaan apabila perbedaan antara kebijakan optimal, tanpa informasi yang sempurna dan kebijakan optimal menggunakan informasi yang sempurna dapat dinyatakan dengan jelas. Pengukuran suatu nilai informasi biasanya dihubungkan dengan Analisis Cost Effectiveness atau Cost Benefit. </p></div>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Lebih lanjut, Davis mengemukakan bahwa ada 10 sifat yang dapat menentukan nilai informasi. Adapun 10 (sepuluh) sifat yang dapat menentukan nilai informasi yaitu sebagai berikut:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kemudahan dalam memperoleh</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi memperoleh nilai yang lebih sempurna apabila diperoleh secara mudah. Informasi yang penting dan sangat dibutuhkan sangat dibutuhkan menjadi tidak bernilai jika sulit diperoleh.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Luas dan Lengkap</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi mempunyai nilai yang lebih sempurna apabila mempunyai lingkup atau cakupan yag luas dan lengkap. Informasi yang tidak lengkap menjadi tidak bernilai, karena tidak dapat digunakan secara baik.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Ketelitian</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi mempunyai nilai yang lebih sempurna apabila mempunyai ketelitian yang tinggi atau akurat. Informasi menjadi tidak bernilai jika tidak akurat, karena akan mengakibatkan kesalahan pengambilan keputusan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kecocokan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi mempunyai nilai yang lebih sempurna apabila sesuai dengan kebutuhan penggunanya. Informasi menjadi berharga dan penting menjadi tidak bernilai jika tidak sesuai dengan kebutuhan enggunanya, karena tidak dapat dimanfaatkan untuk pengambilan keputusan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Ketepatan Waktu</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi mempunyai nilai yang lebih sempurna apabila dapat diterima oleh pengguna pada saat yang tepat. Informasi berharga dan penting menjadi  tidak bernilai jika terlambat diterima atau usang, karena tidak dapat dimanfaatkan pada saat pengambilan keputusan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Kejelasan (Clarity)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi yang jelas akan meningkatkan kesempurnaan nilai informasi. Kejelasan informasi diengaruhi oleh bentuk format informasi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Fleksibilitas atau Keluwasannya</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Nilai informasi semakin sempurna apabila memiliki fleksibilitas tinggi. Fleksibilitas informasi diperlukan oleh para manajer atau pemimpin pada saat pengambilan keputusan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Dapat Dibuktikan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Nilai informasi semakin sempurna apabila informasi tersebut dapat dibukktikan kebenarannya. Kebenaran informasi bergantung pada validitas dan relevan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Tidak Ada Prasangka</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Nilai informasi semakin semourna apabila informasi tersebut tidak menimbulkan prasangka dan keraguan adanya kesalahan informasi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Dapat Diukur</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Informasi untuk pengambilan keputusan harusnya dapat diukur agar dapat mecapai nilai yang sempurna.</p>
</ol>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berdasarkan uraian diatas, maka dapat disimpulkan bahwa Nilai Informasi merupakan perilaku yang digunakan dalam mengambil keputusan untuk memperoleh sebuah informasi yang dapat diukur dan dibuktikan.</p></div>

===Konsep Dasar Sistem Informasi===
====Definisi Sistem Informasi====

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Berikut ini ada dua definisi dari sistem informasi, yaitu:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sutarman (2012:13)<ref name="sutarman"/>, sistem Informasi adalah sistem yang dapat didefinisikan dengan mengumpulkan, memproses, menganalisis, menyebarkan informasi untuk tujuan tertentu. Seperti sistem lainnya, sebuah informasi terdiri atas input (data, intruksi) dan output (laporan, kalkulasi).</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Laudon dalam Yakub (2014:34)<ref name="yakubvico"/>, sistem informasi merupakan sekumpulan komponen yang saling berhubungan untuk mengumoulkan atau mendapatkan, menyimpan, dan mendistribuskan informasi guna menunjang pengambilan keputusan dalam organisasi.</p></li>
</ol>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Berdasarkan kedua pendapat diatas, dapat ditarik kesimpulan bahwa Sistem Informasi adalam suatu sistem dalam suatu organisasi yang digunakan untuk menunjang pengambilan keputusan dengan cara mengumpulkan, memproses, dan menganalisis.</p></div>

====Macam-macam Sistem Informasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Tohari (2013:8)<ref name="tohari"/> menjelaskan terdapat empat jenis sistem informasi yang dianggap dapat menunjang efektivitas dan efesiensi suatu perusahan. Empat jenis sistem informasi tersebut sudah jamak terdapat disebuah perusahaan. Keempat jenis tersebut yakni:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sistem Informasi Akuntansi, yaitu sistem informasiyang menyajikan informasi yang dipakai oleh pemakai fungsi akuntansi. Sistem ini mencakupsemua transaksi yang berhubungan dengan keuangan di sebuah perusahaan atau organisasi.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sistem Informasi Manufaktur, yaitu sistem informasi yang bekerja sama dengan sistem informasi lain untuk mendukung manajemen perusahaan dalam menyelesaikan masalah yang berhubungan dengan produk atau jasa yang dihasilkan perusahaan.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sistem Informasi SDM, yaitu Sistem Informasi yang digunakan oleh perusahaan khususnya di bagian personalia.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sistem informasi Pemasaran, yaitu Sistem Informasu yang menyediakan informasi yang dibutuhkan oleh fungsi pemasaran.</p></li>
</ol>

====Komponen Sistem Informasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Komponen-komponen yang terdapat di dalam semua jenis sistem informasi mencakup tujuh poin. Berikut ketujuh komponen tersebut beserta dengan penjelasan masing-masing (Pratama, 2014:11)<ref name="pratama"/> : </p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Input (masukan)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Sebuah informasi berasal dari data yang telah diolah dan diverifikasi sehingga akurat, bermanfaat, dan memiliki nilai. Komponen input ini berfungsi untuk menerima semua input (masukan) dari pengguna, inputan yang diterima dalam bentuk data. Data ini berasal dari satu maupun beberapa buah sumber.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Output (Keluaran) </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Sebuah sistem informasi akan menghasilkan keluaran (Output) berupa informasi. Komponen output berfungsi untuk menyajikan hasil akhir ke pengguna sistem informasi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Software (Perangkat Lunak) </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Komponen software (perangkat lunak) mencakup semua perangkat lunak yang digunakan didalam sistem informasi. Adanya komponen perangkat lunak ini akan membantu sistem infromasi didalam menjalankan tugasnya dan untuk dapat dijalankan sebagaimana mestinya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Hardware (Perangkat Keras) </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Komponen hardware (perangkat keras) mencakup semua perangkat keras komputer yang digunakan secara fisik di dalam sistem informasi, baik dikomputer server maupun di komputer klient.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Database (Basis Data) </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Komponen basis data berfungsi untuk menyimpan semua data dan informasi kedalam satu atau beberapa tabel. Setiap tabel memiliki field masing-masing. Setiap table memiliki fungsi penyimpanan masing-masing, serta antartabel dapat juga terjadi relasi (hubungan).</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Kontrol dan prosedur</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Kontrol dan prosedur adalah dua buah komponen yang menjadi satu. Komponen kontrol berfungsi untuk mencegah terjadinya beragam gangguan dan ancaman terhadap data dan informasi yang ada di dalam sistem informasi, termasuk juga sistem informasi itu sendiri beserta fisiknya (dalam hal ini komputer server).</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Teknologi dan Jaringan Komputer </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Komponen terakhir di dalam sistem informasi ini, yaitu teknologi jaringan komputer, memegang peranan terpenting untuk sebuah sistem informasi. Komponen teknologi mengatur software, hardware, database, kontrol dan prosedur, input, dan output.</p>
</ol>

===Definisi Perancangan Sistem===
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Dalam membuat sistem, perlu dibuatnya suatu perancangan sistem. Berikut ini ada dua definisi perancangan sistem, yaitu:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Josi dalam Jurnal UISU Vol. 2 No. 1 (2017:69)<ref name="josi">Josi, Ahmat. 2017. Aplikasi Penggajian Pada Perguruan Tinggi (Studi Kasus Sekolah Tinggi X Y Z). Jurnal UISU Vol. 2 No. 1. Medan. Universitas Islam Sumatera Utara. ISSN: 2540-7600.</ref> Perancangan Sistem ialah suatu kondisi dimana pengembang membuat skema aplikasi yang akan dibangun, yang didasarkan pada hasil dari analisis.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    McLeod dalam Fauzi dkk dalam Jurnal Surya Infromatika Vol.1 No.1 (2015:26)<ref name="fauzi">Fauzi, Moch.Agita, Titits Aji Wicaksono. 2015. System Inventory Control Pada Laboratorium Komputer SMK Muhammadiyah Kajen Berbasis Web dengan Framework Codeigniter. ISSN: 2477-3042. Surya Informatika Vol.1 No.1 November 2015.</ref>, Perancangan sistem adalah penentuan proses dan data yang diperlukan oleh sistem baru, jika sistem itu berbasis komputer, perancangan dapat dinyatakan spesifikasi peralatan yang digunakan.</p></li>
</ol>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berdasarkan kedua pengertian diatas maka dapat disimpulkan, perancangan sistem merupakan kondisi dimana ada pengembangan sebuah sistem atau aplikasi yang akan dibangun yang sebelumnya telah dianalisis untuk menghasilkan suatu sistem informasi berbasis komputer yang lebih baik lagi.</p></div>

===Konsep Dasar Analisis Sistem===
====Definisi Analisis Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berikut ini ada dua pendapat para ahli mengenai definisi analisis sistem, yaitu:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Kristanto dalam Sunyoto (2012:210)<ref name="sunyoto">Sunyoto, Danang. 2014. Konsep Dasar Riset Pemasaran & Perilaku Komsumen. Yogyakarta: CAPS.</ref>, analisis sistem adalah orang yang mempunyai kemampuan untuk menganalisis sebuah sistem, memilih alternative pemecahan masalah dan menyelesaikan masalah tersebut dengan menggunakan komputer.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Yogiyanto dalam Sunyoto (2014:210)<ref name="sunyoto"/>, analisis sistem adalah penguraian dari suatu sistem informasi yang utuh ke dalam bagian-bagian komponennya dengan maksud untuk mengidentifikasi dan mengevaluasi permasalahan, kesempatan, hambatan yang terjadi dan kebutuhan yang diharapkan sehingga dapat diusulkan perbaikan.</p></li>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berdasarkan kedua pendapat diatas, maka dapat disimpulkan bahwa, analisis sistem adalah seorang yang memiliki kemampuan untuk menganalisis sebuah sistem yang berfungsi untuk memecahkan masalah.</p></div>
</ol>

====Tahapan Analisis Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut Murad dalam Jurnal CCIT (2013:51)<ref name="murad">Murad, Dina, Fitria. 2013. Aplikasi Intelligence Website Untuk Penunjang Laporan PAUD Pada Himpaudi Kota Tangerang. Jurnal CCIT. Tangerang: Perguruan Tinggi Raharja.</ref>, tahap analisis merupakan tahap dalam mencari informasi sebanyak-banyaknya mengenai sistem yang diteliti dengan melakukan metode-metode pengumpulan data sehingga ditemukan kelebihan dan kekurangan sistem serta user requirement. Selain itu, tahap ini juga dilakukan untuk mencari pemecah masalah dan menganalisa bagaimana sistem akan dibangun untuk memecahkan masalah pada sistem sebelumnya.</p></div>

====Fungsi Analisis Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Tohari (2013:11)<ref name="tohari"/> menyatakan bahwa:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Mengidentifikasi masalah-masalah kebutuhan user</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Menyatakan secara spesifik sasaran yang harus dicapai untuk memenuhi kebutuhan user</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Memilih alternatif-a;ternatif metode pemecahan masalah</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Merencanakan dan menerapkan rancangan sistemnya sesuai dengan permintaan user.</p></li>
</ol>

==Teori Khusus==
===Konsep Dasar Koperasi===
====Definisi Koperasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut Undang-Undang No. 25 tahun 1992, Koperasi adalah badan usaha yang beranggotakan orang-orang atau badan hukum koperasi dengan melandaskan kegiatannya berdasarkan prinsip koperasi sekaligus sebagai gerakan ekonomi rakyat yang berdasarkan asas kekeluargaan. Koperasi bertujuan untuk menyejahterakan anggotanya. Berdasarkan pengertian tersebut, yang dapat menjadi anggota koperasi yaitu: Perorangan, yaitu orang yang secara sukarela menjadi anggota koperasi, Badan hukum koperasi, yaitu suatu koperasi yang menjadi anggota, koperasi yang memiliki lingkup lebih luas
</p></div>

====Fungsi dan Peran Koperasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut Undang-undang No. 25 tahun 1992 Pasal 4 dijelaskan bahwa fungsi dan peran koperasi sebagai berikut:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Membangun dan mengembangkan potensi dan kemampuan ekonomi anggota pada khususnya dan masyarakat pada umumnya untuk meningkatkan kesejahteraan ekonomi dan sosialnya;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Berperan serta secara aktif dalam upaya mempertinggi kualitas kehidupan manusia dan masyarakat;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Memperkokoh perekonomian rakyat sebagai dasar kekuatan dan ketahanan perekonomian nasional dengan koperasi sebagai soko-gurunya;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Berusaha untuk mewujudkan dan mengembangkan perekonomian nasional, yang merupakan usaha bersama berdasarkan atas asas kekeluargaan dan demokrasi ekonomi;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Mengembangkan kreativitas dan membangun jiwa berorganisasi bagi para pelajar.</p></li>
</ol>

====Prinsip Koperasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut UU No. 25 tahun 1992 Pasal 5 disebutkan beberapa prinsip koperasi, yaitu:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Keanggotaan bersifat sukarela dan terbuka;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Pengelolaan dilakukan secara demokratis;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Pembagian Sisa Hasil Usaha (SHU) dilakukan secara adil sebanding dengan besarnya jasa usaha masing-masing anggota (andil anggota tersebut dalam koperasi);</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Pemberian balas jasa yang terbatas terhadap modal;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Kemandirian;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Pendidikan perkoprasian;</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Kerjasama antar koperasi.</p></li>
</ol>

====Jenis-jenis Koperasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut UU Nomor 25 Tahun 1992 Tentang Perkoperasian Koperasi secara umum dapat dikelompokkan menjadi koperasi konsumen, koperasi produsen dan koperasi kredit (jasa keuangan). Koperasi dapat pula dikelompokkan berdasarkan sektor usahanya, yaitu:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Koperasi Simpan Pinjam</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Koperasi Simpan Pinjam Adalah koperasi yang bergerak di bidang simpanan dan pinjaman.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Koperasi Konsumen</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Koperasi Konsumen Adalah koperasi beranggotakan para konsumen dengan menjalankan kegiatannya jual beli menjual barang konsumsi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Koperasi Produsen</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Koperasi Produsen Adalah koperasi beranggotakan para pengusaha kecil (UKM) dengan menjalankan kegiatan pengadaan bahan baku dan penolong untuk anggotanya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Koperasi Pemasaran</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Koperasi Pemasaran Koperasi yang menjalankan kegiatan penjualan produk/jasa koperasinya atau anggotanya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Koperasi Jasa</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Koperasi Jasa Koperasi yang bergerak di bidang usaha jasa lainnya.</p>
</ol>

====Sumber Modal Koperasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Seperti halnya bentuk badan usaha yang lain, untuk menjalankan kegiatan usahanya koperasi memerlukan modal. Adapun modal koperasi terdiri atas modal sendiri dan modal pinjaman.
Modal sendiri meliputi sumber modal sebagai berikut:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Simpanan Pokok</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Simpanan pokok adalah sejumlah uang yang wajib dibayarkan oleh anggota kepada koperasi pada saat masuk menjadi anggota. Simpanan pokok tidak dapat diambil kembali selama yang bersangkutan masih menjadi anggota koperasi. Simpanan pokok jumlahnya sama untuk setiap anggota.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Simpanan Wajib</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Simpanan wajib adalah jumlah simpanan tertentu yang harus dibayarkan oleh anggota kepada koperasi dalam waktu dan kesempatan tertentu, misalnya tiap bulan dengan jumlah simpanan yang sama untuk setiap bulannya. Simpanan wajib tidak dapat diambil kembali selama yang bersangkutan masih menjadi anggota koperasi.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Simpanan khusus/lain-lain</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    misalnya:Simpanan sukarela (simpanan yang dapat diambil kapan saja), Simpanan Qurba, dan Deposito Berjangka.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Dana Cadangan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Dana cadangan adalah sejumlah uang yang diperoleh dari penyisihan Sisa Hasil usaha, yang dimaksudkan untuk pemupukan modal sendiri, pembagian kepada anggota yang keluar dari keanggotaan koperasi, dan untuk menutup kerugian koperasi bila diperlukan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Hibah</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Hibah adalah sejumlah uang atau barang modal yang dapat dinilai dengan uang yang diterima dari pihak lain yang bersifat hibah/pemberian dan tidak mengikat.
Adapun modal pinjaman koperasi berasal dari pihak-pihak sebagai berikut:</p>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    a. Anggota dan calon anggota</p></div>
