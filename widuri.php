    Menurut Yasin (2012:268)<ref name="yasin">Yasin, Ferdi. 2012. Rekayasa Perangkat Lunak Berorientasi Objek. Jakarta: Mitra Wacana Media.</ref>, tujuan UML diantaranya adalah:</p></div>
