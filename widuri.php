==Latar Belakang==


<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Bisnis sewa kelola apartemen di Jakarta saat ini sangat banyak,hal ini disadari oleh setiap pemilik apartemen sehingga  merekaberlomba-lomba untuk melakukan terobosan agar   bisnis  yangdijalankannya  dapat bersaing   dan terserap  di pangsa  pasar  Jakarta.Sebagai kota bisnis dengan tingkat  pertumbuhan dan lalu-lintas ekonomi
    yang cukup tinggi  hal ini  menjadikan  bisnis persewaan apartemen
    sangat menjanjikan. </p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Ide bisnis ini muncul karena meningkatnya daya beli masyarakat
    terhadap properti khususnya apartemen. Developer real estate bersaing
    ketat menyediakan hunian exclusive di tengah pusat kota dengan harga
    yang kompetitif di bandingkan hunian rumah. Hal ini membuat
    masyarakat semakin tertarik untuk berinvestasi dalam bidang properti.
    Disisi lain kalangan wirausahawan melihat ini sebagai peluang untuk
    memfasilitasi masyarakat yang telah berinvestasi dalam bentuk
    apartemen dengan cara mengelola apartemen tersebut untuk disewakan
    kepada pelanggan dalam hal ini adalah kalangan traveler ataupun turis
    domestik dengan menjadikan apartemen yang bisa di sewa harian
    layaknya hotel dengan benefit fasilitas yang lebih daripada hotel. </p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2"> Oleh karena itu Perusahaan harus bisa membuat suatu sistem
        yang dapat mempermudah pelanggan untuk menyewa apartemen yang
        bisa disewa secara harian sebagai alternatif menginap daripada hotel. </p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">Pelayanan yang baik terhadap pelanggan menjadi syarat
