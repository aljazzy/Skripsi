==Tata Laksana Sistem Yang Berjalan==
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Ada beberapa prosedur yang harus dilakukan dalam proses pengajuan kredit sampai dengan proses pencairan yang sedang berjalan saat ini, yaitu:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Mendaftar menjadi anggota</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Sebelum megajukan permohonan pinjaman, diharuskan kepada calon peminjam harus terdaftar menjadi anggota koperasi terlebih dahulu. Setelah terdaftar menjadi anggota koperasi kurang lebih 6 bulan, maka sudah diperbolehkan mengajukan permohonan pinjaman.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Prosedur pengajuan permohonan pinjaman</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Surat permohonan pinjaman diajukan kepada bagian staf kredit lengkap dengan bukti-bukti pendukung lainnya seperti: fotocopy KTP (suami/istri), fotocopy kartu keluarga, fotocopy surat nikah, fotocopy sertifikat diksar, fotocopy rekening listrik dan telepon, fotocopy slip gaji dan mencantumkan alamat tingggal sekarang, buku rekening simpanan, fotocopy kepemilikan barang jaminan, fotocopy surat perjanjian kerja, serta bagi yang memiliki usaha wajib melampirkan fotocopy Surat Izin Usaha Perdagangan (SIUP). Data yang telah dikumpulkan tersebut kemudian di analisis oleh bagian staf kredit, informasi lainnya tentang kondisi pemohon kredit yang dilakukan oleh staf kredit dan hasil analisa kredit harus ditandatangani oleh manajer TP (Tempat Pelayanan) atau kepala kantor dan staf kredit. Analisa yang dilakukan seperti, batas maksimum permohonan pinjaman, kelayakan barang jaminan, analisa 5C (Capacity, Character, Capital, Condition, dan Collateral).</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Investigasi kepada pemohon pinjaman</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Setelah tahap mengajukan surat permohonan pinjaman, pemohon pinjaman harus menyatakan kesediaannya memasuki tahap investigasi dari permohonan pinjaman tersebut. Staf pinjaman yang ditugasi melakukan investigasi mengunjungi tempat tinggal anggota yang melakukan pemohon pinjaman tersebut serta mengunjungi tempat tinggal para penjamin (yang merekomendasikan) untuk memvalidasi informasi yang ditulis didalam surat permohonan pinjaman.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Evaluasi Pinjaman</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Surat permohonan pinjaman yang sudah dilakukan investigasi pinjaman dan prosedur penilaian pinjaman disampaikan kepada Kepala Kantor untuk diteliti ulang. Jika tidak ada perubahan, dan Kepala Kantor menyatakan setuju maka selanjutnya permohonan pinjaman tersebut disampaikan kedapa kasir (bagian keuangan) untuk di tindak lanjuti.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Penilaian Barang Jaminan</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Semua barang jaminan harus terlebih dahulu diteliti dan dinilai. Penelitian harus dilakukan oleh tim penilai dari CU Bererod Gratia.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Pencairan Dana Pinjaman</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Kepala Kantor yang merupakan wewenangnya akan menyetujui suatu permohonan pinjaman. Daftar rinci dari semua permohonan pinjaman yang sudah disetujui harus disampaikan kepada General Manajer/ Deputy Usaha/ Manajer TP/ Kepala Kantor TP untuk menandatangani kwitansi atau slip pencairan pinjaman, dokumen tersebut dikembalikan kepada staf kredit untuk diarsipkan, sedangkan kwitansi atau slip pencairan kredit akan dibeikan kepada kasir untuk dibukukan dan dokumen resmi lainnya untuk diarsipkan.</p>
</ol>



