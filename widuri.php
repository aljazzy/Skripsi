===Sejarah Singkat Koperasi CU Bererod Gratia===
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Sejarah Awal pembentukan Credit Union bererod Gratia dimulai dari Pesta Natal dan Tahun Baru keluarga besar karyawan-karyawati KWI (konferensi Waligereja Indonesia) pada tahun 2002. Tema Natal pada waktu itu adalah “Pentingnya kebersamaan yang berkesinambungan yang saling mensejahterakan” Dalam Acara tersebut dibahas bagaimana para karyawan dan karyawati dalam satu komunitas KWI dapat saling mensejahterakan satu dengan yang lain. Dari perbincangan tersebut timbul gagasan untuk membentuk wadah/paguyuban saling mensejahterakan antar karyawan. </p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Diskusi bentuk wadah kesejahtaraan antar karyawan terus berkembang mulai dengan rencana membentuk, paguyuban, arisan, koperasi, Credit Union dan seterusnya. Akhirnya pada hari Selasa 21 Maret 2006 ketua PSDM KWI Romo Maryo SJ mengundang INKOPDIT yang diwakili Bpk. Abbat Elias SE dan Bernard Situngkir. Mereka berdua didampingi Sekretaris Komisi PSE KWI, Rm Stepanus Bijanta CM untuk melakukan sosialisasi Credit Union di tengah karyawan-karyawati KWI.  Tidak lama dari sosialisasi itu, kemudian tokoh-tokoh KWI memberanikan diri untuk memulai mengadakan RENSTRA (Rencana Starategis) Credit Union pada tanggal 11-14 Mei 2006 dengan mengundang Bapak A. R. Mecer, pelopor Credit Union di Kalimantan yang juga ketua  BK3D Kalimantan. Dari pertemuan RENSTRA ini akhirnya pada tanggal  15 Mei 2006 dibentuk Credit Union Bererod Gratia. KWI  memberikan ruangan gratis di Gedung KWI jl. Cikini Raya 2 No. 10 untuk dapat digunakan sebagai tempat pelayanan awal CU Bererod Gratia.</p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">Kemudian setelah berkembang cukup pesat, 3 tahun kemudian kantor CU Bererod Gratia pindah dan mampu menyewa gedung KWI lain yang lebih besar yaitu di Jl. Matraman no. 31 Jakarta Timur. Dalam sewindu pelayanannya, akhirnya CU Bererod Gratia memberanikan membeli sebuah ruko 4 lantai di Jl. Utan Kayu Raya no. 46C, yang kini menjadi kantor Pusat CU Bererod Gratia untuk melayani 13 tempat pelayanan yang tersebar di Jakarta, Bekasi, Tangerang dan Jawa Tengah. Dimulai dengan karywan-karyawati KWi sebagai Anggota, kini Anggota CU Bererod Gratia juga berasal dari masyarakat luas dari berbagai etnik, budaya dan agama yang berbeda-beda, Kata ”Bererod” diambil dari bahasa Betawi yang berarti BERIRINGAN dan ”Gratia” dari bahasa Latin yang artinya RAHMAT.  Makna dari kata Bererod Gratia adalah Beriringan dalam Rahmat Tuhan. </p></div>

