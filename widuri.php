==Teori Umum==
===Konsep Dasar Sistem===
====Definisi Sistem====

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Istilah sistem sering digunakan untuk menggambarkan hubungan suatu kesatuan yang terdiri dari komponen-komponen yang saling berinteraksi untuk mencapai suatu tujuan. Berikut ini adalah beberapa pengertian sistem menurut para ahli, yaitu sebagai berikut:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Winanti (2014:4)<ref name"winanti">Winanti, Sri dan Nila Aprila. 2014. Pengaruh Pemahaman Sistem Akuntansi Keuangan Daerah, Pemahaman Pengelolaan Keuangan Daerah, Pelatihan, Dan Komitmen Organisasi terhadap Kinerja Pengelola Keuangan Satuan Kerja Perangkat Daerah. Skripsi Fakultas Ekonomi Dan Bisnis. Bengkulu: Universitas Bengkulu.</ref>, sistem adalah seperangkat komponen yang saling berhubungan dan salig bekerja sama untuk mencapai tujuan. </p>
</li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Susanto (2013:22)<ref name="susanto">Susanto, Azhar. 2013. Sistem Informasi Akuntansi. Bandung: Lingga Jaya.</ref>, sistem adalah kumpulan/group dari sub sistem/bagian/komponen apapun baik phisik ataupun non phisik yang saling berhubungan satu sama lain dan bekerja sama secara harmonis untuk mencapai satu tujuan tertentu. </p>
</li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Tohari (2013:2)<ref name="tohari">Tohari, Hamim. 2013. Analisis Serta Perancangan Sistem Informasi Melalui Pendekatan UML. Yoyakarta: Andi.</ref>, sistem adalah kumpulan atau himpunan dari unsur atau variabel-variabel yang saling terkait, saling berinteraksi, dan saling tergantung sama lain untuk mencapai tujuan.</p>
</li>
</ol>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Berdasarkan beberapa pengertian diatas, maka dapat disimpulkan bahwa sistem adalah prosedur yang saling berhubungan yang terintegrasi antara satu komponen dengan komponen lain untuk mencapai suatu tujuan tertentu.</p></div>

====Karakteristik Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Tohari (2013:2)<ref name="tohari"/> menyatakan bahwa karakteristik suatu sistem sebagai berikut:</p></div>

<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Komponen Sistem (Components) </p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Suatu sistem terdiri dari komponen-komponen yang saling berinteraksi yang artinya saling bekerja sama membentuk satu kesatuan.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Batasan Sistem (Boundary System)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Batas sistem merupakan daerah yang membatasi antara sistem yang satu dengan sistem yang lainnya atau dengan lingkungan luarnya. Adanya batas sistem, maka sistem dapat membentuk suatu kesatuan, karena dengan sistem ini, fungsi dan tugas dari subsistem satu dengan yang lainnya berbeda tetapi tetap saling berinteraksi. Dengan kata lain, batas sistem merupakan ruang lingkup atau scope dari sistem atau subsistem itu sendiri.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Lingkungan Luar Sistem (Environment)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Lingkungan dari sistem adalah apapun di luar batas dari sistem yang mempengaruhi operasi suatu sistem. Lingkungan luar sistem dapat bersifat menguntungkan dan juga dapat merugikan. Lingkungan luar sistem yang menguntungkan Sedangkan, lingkungan luar yang merugikan harus dihilangkan supaya tidak mengganggu operasi dari sistem.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Penghubung Sistem (Interface)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Penghubung merupakan suatu media (penghubung) antara satu subsistem dengan subsistem lainnya yang membentuk satu kesauan, sehingga sumber-sumber daya mengalir dari subsistem yang satu ke subsistem yang lainnya. Dengan kata lain, melalui penghubung, output dari subsistem akan menjadi input bagi subsistem lainnya.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Masukan (Input)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Input adalah energi atau sesuatu yang dimasukkan ke dalam suatu sistem yang dapat berupa masukkan yaitu energi yang dimasukkan supaya sistem dapat beroperasi atau masukkan sinyal yang merupakan energi yang di proses untuk menghasilkan suatu luaran.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Keluaran (Output)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Merupakan hasil dari energi yang diolah dan diklasifikasikan menjadi luaran yang berguna, juga merupakan luaran atau tujuan akhir dari sistem.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Pengolah (process)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Suatu sistem dapat mempunyai suatu bagian pengolah yang akan mengubah input menjadi output.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sasaran (Objective)</p>
</li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Sasaran dari sistem sangat menentukan sekali masukkan yang dibutuhkan sistem dan keluaran yang akan dihasilkan sistem. Suatu dikatakan berhasil bila mengenai sasaran atau tujuannya.</p>
</ol>

====Klasifikasi Sistem====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in">
<p style="line-height: 2">
    Sistem dapat diklasifikasikan dari beberapa sudut pandang diantaranya (Taufiq, 2013:8)<ref name="taufiq">Taufiq, Rohmat. 2013. Sistem Informasi Manajemen. Graha Ilmu: Yogyakarta.</ref> :</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem Abstrak (Abstract System) dan Sistem Fisik (Physical System). Sistem abstrak merupakan sistem yang berupa pemikiran atau ide-ide yang tidak tampak secara fisik. Misalnya sistem teologi, yaitu sistem yang berupa pemikiran-pemikiran hubungan antara manusia dengan Tuhan. Sistem fisik merupakan sistem yang ada secara fisik. Misalnya sistem komputer, sistem produksi, dan sistem transportasi.</p>
</li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">System Alamiah (Natural System) dan system Buatan Manusia (Human Made System) Sistem alamiah adalah system yang terjadi melalui proses alam, tidak dibuat manusia. Misalnya system perputaran bumi. Sistem buatan manusia adalah system yang melibatkan interaksi manusia dengan mesin yang disebut human machine system. Misalnya system informasi berbasis komputer.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem dapat dipastikan dan Sistem tidak dapat dipastikan. Sistem dapat dipastikan merupakan suatu sistem yang input proses dan outputnya sudah ditentukan sejak awal. Sudah dideskripsikan dengan jelas apa inputannya bagaimana cara prosesnya dan harapan yang menjadi outputnya seperti apa. Sedangkan sistem tidak dapat dipastikan atau sistem probabilistik merupakan sebuah sistem yang belum terdefinisi dengan jelas salah satu dari input-proses-output atau ketiganya belum terdefinisi dengan jelas.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify">
<p style="line-height: 2">Sistem tertutup dan Sistem terbuka. Sistem tertutup dan sistem terbuka yang membedakan adalah ada faktor-faktor yang mempengaruhi dari luar sistem atau tidak, jika tidak ada faktor-faktor yang mempengaruhi dari luar itu bisa disebut dengan sistem tertutup tapi jika ada pengaruh komponen dari luar disebut sistem terbuka.</p></li>

