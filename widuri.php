===Konsep Dasar Kredit===
====Definisi Kredit====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Berikut ini ada dua pendapat para ahli mengenai definisi dari Kredit, diantaranya yaitu:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Berdasarkan Undang-Undang Nomor 10 Tahun 1998, kredit adalah penyediaan uang atau tagihan yang dipersamakan dengan itu, berdasarkan persetujuan atau kesepakatan pinjam meminjam antara bank dengan pihak lain yang diwajibkan pihak peminjam untuk melunasi hutangnya setelah jangka waktu tertentu dengan jumlah bunga, imbalan atau pembagian hasil keuntungan. </p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Menurut Kasmir (2013:98)<ref name="kasmir">Kasmir. 2013. Bank dan Lembaga Keuangan Lainnya, edisi revisi, cetakan 12. Jakarta: Rajawali Pers.</ref>, kredit dalam pengertian umum adalah bahwa kredit diserahkan kepada kepercayaan atas kemampuan si peminjam untuk membayar sejumlah uang pada masa yang akan datang.</p></li>
</ol>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Berdasarkan dua pendapat diatas, dapat disimpulkan definisi kredit adalah penyediaan uang atau tagihan yang diserahkan atas kemampuan si peminjam untuk membayar pada masa yang akan datang.</p></div>

====Unsur-unsur Kredit====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Menurut Rivai (2013:198)<ref name="rivai">Rivai, Veithzal dan Andria Permata Veithzal. 2013. Credit Management Handbook. Jakarta: PT. Raja Grafindo Persada.</ref> unsur-unsur yang terdapat dalam kredit adalah sebagai berikut:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat dua pihak, yaitu pemberi kredit (kreditur) dan penerima kredit (debitur). </p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat kepercayaan pemberi kredit kepada penerima kredit yang didasarkan atas credit rating penerima kredit</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat persetujuan, berupa kesepakatan pihak bank dengan pihak lainnya yang berjanji membayar dari penerima kredit kepada pemberi kredit.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat penyerahan barang, jasa atau uang dari pemberi kredit kepada penerima kredit</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat unsur waktu (time element). </p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat unsur resiko (degree of risk) baik di pihak pemberi kredit maupun di pihak penerima kredit.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Terdapat unsur bunga sebagai kompensasi (prestasi) kepada pemberi kredit.</p></li>
</ol>

====Fungsi dan Tujuan Kredit====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Fungsi kredit menurut Rivai (2013:200)<ref name="rivai"/> didalam perekonomian, perdagangan, dan keuangan dapat dikemukakan sebagai berikut:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Meningkatkan utility (daya guna) dari modal/uang.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Meningkatkan utility (daya guna) suatu barang.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Meningkatkan peredaran dan lalu lintas uang.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Menimbulkan gairah berusaha masyarakat.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Alat stabilitas ekonomi.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Jembatan untuk peningkatan pendapatan nasional.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 2">
    Sebagai alat meningkatkan hubungan ekonomi internasional.</p></li>
</ol>

