<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
Use Case Diagram<br>
Menurut Murad dalam Jurnal CCIT (2013:57)<ref name="murad"/>, Diagram Use Case adalah diagram yang bersifat status yang memperlihatkan himpunan use case dan aktor-aktor (suatu jenis khusus dari kelas). Diagram ini memiliki 2 fungsi, yaitu mendefinisikan fitur apa yang harus disediakan oleh sistem dan menyatakan sifat sistem dari sudut pandang user.
</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Activity Diagram<br>
Berikut ini merupakan definisi dari activity diagram, yaitu:<br>
a.  Murad dalam Jurnal CCIT (2013:53)<ref name="murad"/>, Activity diagram merupakan diagram yang bersifat dinamis. Activity diagram adalah tipe khusus dari diagram state yang memperlihatkan aliran dari suatu aktifitas ke aktifitas lainnya dalam suatu sistem dan berfungsi untuk menganalisa proses.<br>
b.  Menurut Vidia (2013:20)<ref name="vidia">Vidia. Dhanada, dkk. 2013. Analisis Dan Perancangan Sistem Informasi Rawat Jalan Di Rumah Sakit Hewan Universitas Airlangga Surabaya Dengan Metode Berorientasi Objek. Surabaya: Universitas Airlangga.</ref>, Activity diagram dibuat berdasarkan aliran dasar dan aliran alternatif pada skenario use case diagram. Pada activity diagram digambarkan interaksi antara aktor pada use case diagram dengan sistem.
</p></li>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berdasarkan dua pendapat diatas dapat disimpulkan bahwa actvity diagram adalah diagram yang dibuat berdasarkan skenario pada use case diagram dalam suatu sistem yang berfungsi untuk menganalisa proses.</p></div>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Sequence Diagram<br>
Menurut Vidia (2013:21)<ref name="vidia"/>, Sequence diagram dibuat berdasarkan activity diagram dan class diagram. Sequence diagram menggambarkan aliran pesan yang terjadi antar kelas yang dideskripsikan pada class diagram dengan menggunakan operasi yang dimiliki kelas tersebut. Untuk aliran pesan, sequence diagram merujuk pada alur sistem activity diagram yang telah dibuat sebelumnya.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
Class Diagram<br>
Menurut Wijayanto (2013:33)<ref name="wijayanto">Wijayanto. Tegar, dkk. 2013. Analisis Dan Perancangan Sistem Informasi Pemesanan Dan Penjualan Barang Dengan Metode Berorientasi Objek Di U.D. Aneka Jaya Surabaya. Surabaya: Universitas Airlangga.</ref>, Class diagram dibuat berdasarkan use case diagram dan activity diagram yang telah dibuat, maka dapat diperoleh kelas-kelas yang digunakan dalam sistem.</p></li>
</ol>

