===Konsep Dasar Elisitasi===
====Definisi Elisitasi====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Berikut ini merupakan dua definisi dari elisitasi, yaitu:</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Fatkurrotin dkk dalam jurnal JSIKA Vol. 4 No. 1 (2015:10) Elisitasi kebutuhan (pengumpulan kebutuhan) adalah tahap pertama yang dilakukan untuk melakukan proses analisis kebutuhan. Proses elisitasi pertama kali dilakukan dengan cara identifikasi permasalahan, yaitu melalui proses wawancara dan observasi. Proses wawancara dilakukan hanya kepada stakeholder yang terkait saja.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Menurut Hidayati dalam Guritno, dkk (2011:302), Elisitasi merupakan rancangan yang dibuat berdasarkan sistem yang baru yang diinginkan oleh pihak manajemen terkait dan disanggupi oleh penulis untuk dieksekusi”. Elisitasi didapat melalui metode wawancara dan dilakukan melalui tiga tahap, yaitu:
<br>
a. Elisitasi tahap I<br>
Berisi seluruh rancangan sistem baru yang diusulkan oleh pihak manajemen terkait melalui proses wawancara.<br>
b. Elisitasi tahap II<br>
Merupakan hasil pengklasifikasian dari elisitasi tahap I berdasarkan metode MDI. Metode MDI ini bertujuan untuk memisahkan antara rancangan sistem yang penting dan harus ada pada sistem baru dengan rancangan yang disanggupi oleh penulis untuk dieksekusi. Berikut penjelasan mengenai Metode MDI:<br>
1) “M” pada metode MDI itu artinya Mandatory (penting). Maksudnya requirement tersebut harus ada dan tidak boleh dihilangkan pada saat membuat sistem baru.<br>
2) “D” pada MDI itu artinya Desirable. Maksudnya requirement tersebut tidak terlalu penting dan boleh dihilangkan. Tetapi jika requirement tersebut digunakan dalam pembentukan sistem, akan membuat sistem tersebut lebih sempurna.<br>
3) “I” pada MDI itu artinya Inessential. Maksudnya bahwa requirement tersebut bukanlah bagian dari sistem yang dibahas dan merupakan bagian luar sistem.<br><br>
c. Elisitasi tahap III<br>
Merupakan hasil penyusutan dari elisitasitahap II dengan cara mengeliminasi semua requirement yang option-nya I pada metode MDI. Selanjutnya semuarequirementyang tersisa diklasifikasikan kembali melalui metode TOE, yaitu sebagai berikut :<br>
1) “T” artinya Teknikal, maksudnya bagaimana tata cara atau teknik pembuatan requirement tersebut dalam sistem yang diusulkan.<br>
2) “O” artinya Operasional, maksudnya bagaimana tata cara penggunaan requirementtersebut dalam sistem yang akan dikembangkan.<br>
3) “E” artinya Ekonomi, maksudnya berapakah biaya yang diperlukan guna membangun requirement tersebut di dalam sistem.<br>
Metode TOE tersebut dibagi kembali menjadi beberapa option, yaitu:<br>
1) High (H): Sulit untuk dikerjakan, karena teknik pembuatan dan pemakaiannya sulit serta biaya mahal. Sehingga requirement tersebut harus dieliminasi. <br>
2) Middle (M): Mampu untuk dikerjakan.<br>
3) Low (L): Mudah untuk dikerjakan.<br><br>
d. Final Draft Elisitasi<br>
Merupakan hasil akhir yang dicapai dari suatu proses elisitasi yang dapat digunakan sebagai dasar pembuatan suatu sistem yang akan dikembangkan.

</p></li>
</ol>

