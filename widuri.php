b. Mengupayakan agar pinjaman lalai berada dibawah 5% dari pinjaman beredar.<br>
c. Melaksanakan proses kredit sesuai Pola Kebijakan dan SOP.<br>
d. Memonitoring dan mengkomunikasikan pada anggota peminjam akan tugas kewajiban dalam melaksanakan angsuran pinjaman.<br>
e. Memastikan tersedianya form pengajuan pinjaman, dan segera berkoordinasi dengan Kepala Kantor bila persediaan hampir habis yang tidak bisa disediakan oleh TP dan harus meminta kantor pusat.</p>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Staf Kasir</p></li>
