===Konsep Dasar Analisa SWOT===
====Definisi SWOT====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Menurut Fatimah (2016:7)<ref name="fatimah">Fatimah, Dwi, Nur’aini, Fajar. 2016. Teknik Analisis SWOT. Yogyakarta: Quadrant.</ref>, Analisis SWOT merupakan akronim atau singkatan dari 4 kata yaitu strengths, weaknesses, opportunities, dan threaths. Analisis SWOT ini merupakan salah satu metode ang digunakan untuk mengevaluasi kekuatan (strengths), kelemahan (weaknesses), peluang (opportinities), dan ancaman (threats) dalam suatu spekulasi bisnis, Beberapa instrumen perencanaan strategis klasik yang memberikan cara sederhana untuk memperkirakan cara terbaik dalam menentukan sebuah stategi. Instrumen ini memudahkan para praktisi untuk menentukan apa yang bisa dicapai, dan hal-hal apa saja yang perlu di perhatikan oleh mereka.</p></div>
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Analisis SWOT merupakan suatu instrumen pengidentifikasian berbagai faktor yang membentuk secara sistematis yang digunakan untuk merumuskan strategi perusahaan. Pendekatan analisis ini didasarkan pada logika yang dapat memaksimalkan kekuatan (strengths), kelemahan (weaknesses), peluang (opportinities), dan ancaman (threats). Secara singkat analisis SWOT dapat diterapkan dengan cara menganalisis dan memilah hal-hal yang mempengaruhi keempat faktornya. Dengan demikian, hasil dari analisis dapat membentuk perencanaan strategi berdasarkan hasil analisis terhadap faktor-faktor strategis perusahaan (kekuatan,kelemahan,peluang, dan ancaman).</p></div>

====Manfaat Analisis SWOT====
<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify;text-indent: 0.5in"><p style="line-height: 2">
    Analisis SWOT dianggap memiliki banyak manfsst dan kelebihan dibandingkan dengan metode analisa yang lain. Berikut perupakan penjabaran beberapa manfaat menggunakan metode analisa SWOT :</p></div>
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Analisis SWOT dapat membantu melihat suatu persoalan dari empat sisi sekaligus yang menjadi dasar sebuah analisis persoalan, yaitu kekuatan, kelemahan, kesempatan atau peluang, dan ancaman.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Analisis SWOT mampu memberikan hasil berupa analisis yang cukup tajam sehingga mampu memberikan arahan ataupun rekomendasi untuk mempertahankan kekuatan sekaligus menambah keuntungan berdasarkan sisi peluang yang ada, sambil mengurangi kekurangan dan juga menghindari ancaman.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Analisis SWOT dapat membantu kita “membedah” organisasi dari empat sisi yang dapat menjadi dasar dalam proses identifikasinya dan dengan analisis ini kita dapat menemukan sisi-sisi yang terkadang terlupakan atau tidak terlihat selama ini.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Analisis SWOT dapat menjadi instrumen yang cukup ampuh dalam melakukan analisis strategi, sehingga dapat menemukan langkah yang tepat dan terbaik sesuai dengan situasi pada saat itu.</p></li>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Analisis SWOT dapat digunakan untuk membantu organisasi meminimalisasi kelemahan yang ada serta menekan munculnya dampak ancaman yang mungkin akan timbul.</p></li>
</ol>


====Faktor-faktor Analisis SWOT====
<ol>
<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Strength (Kekuatan)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Strengths merupakan sebuah kondisi yang menjadi sebuah kekuatan dalam organisasi. Faktor-faktor kekuatan merupakan suatu kompetensi khusus atau sebuah kompetensi keunggulan yang terdapat dalam tubuh organisasi itu sendiri. Faktor-faktor kekuatan tersebut merupakan nilai plus atau keunggulan komperatif dari sebuah organisasi. Hal tersebut muda terlihat apabila sebuah organisasi memiliki hal khusus yang lebih unggul dari pesaing-pesaingnya serta dapat memuaskan stakeholder maupun pelanggan.
<br>
Sebagai contoh dari sisi keunggulan, antara lain kekuatan organisasi yang bergerak dibidang penjualan handphone. Perusahaan tersebut menjual handphone yang khusus memiliki fitur underwater. Fitur underwater tersebut merupakan salah satu strength atau kekuatan yang dimiliki oleh perusahaan handphone tersebut.</p>

<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Weaknesses (Kelemahan)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Weaknesses merupakan kondisi atau segala sesuatu hal yang menjadi kelemahan atau kekurangan yang terdapat dalam tubuh organisasi. Pada dasarnya, sebuah kelemahan merupakan suatu hal yang wajar ada dalam organisasi. Namun yang  terpenting adalah bagaimana organisasi membangun sebuah kebijakan sehingga dapat meminimalisasi kelemahan-kelemahan tersebut atau bahkan dapat menghilangkan kelemahan yang ada.
<br>
Kelemahan ini dapat berupa kelemahan dalam sarana dan prasarana. Kualitas atau kemampuan karyawan yang ada dalam organisasi, lemahnya kepercayaan konsumen, tidak sesuainya antara hasil produksi dengan kebutuhan konsumen atau dunia usaha da industri. Kelemahan yang harus segera dibenahi oleh para stakeholder dalam suatu perusahaan, antara lain yaitu :
<br>
a. Lemahnya SDM dalam organisasi<br>
b. Sarana dan prasarana yang masih sebatas pada sarana wajib saja.<br>
c. Kurangnya sensitivitas dalam menangkap peluang yang ada, sehingga cenderung membuat organisasi mudah puas dengan keadaan yang dihadapi sekarang ini.<br>
d. Output pada produk yang belum sepenuhnya bersaing dengan produk perusahaan yang lain dan sebagainya.
</p>

<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Opportinities (Peluang)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Beberapa situasi yang dapat menjadi peluang sebuah perusahaan antara lain sebagai berikut :<br>
a. Kecenderungan pasar menyukai produk tertentu<br>
b. Identifikasi suatu produk yang belum mendapat perhatian pasar.<br>
c. Perubahan dalam situasi perdagangan dengan parakompetitor<br>
d. Hubungan dengan konsumen<br>
Sedangkan, ada beberapa peluang yang dapat dikembangkan oleh organisasi, atau perusahaan melihat situasi eksternal yang semakin akrab dengan kemajuan teknologi. Peluang tersebut antara lain, yaitu :<br>
a. Di era kemajuan teknologi yang semakin pesat, memudahkan prusahaan untuk mengembangkan usahanya dengan bantuan alat-alat teknologi yang semakin canggih. Apabila dapat dilakukan dengan optimal, maka kemungkinan dapat meminimalisasi penggunaan bantuan tenaga kerja manusia dalam pekerjaan tertentu.<br>
b. Penggunaan media sosial yang semakin akrab di kalangan masyarakat indonesia, memudahkan perusahaan untuk menjadikan media sosial sebagai sarana pemasaran yang cukup efektif.<br>
</p>

<li style="font-size: 12pt;font-family: 'times new roman';text-align: justify; line-height: 2;"><p style="line-height: 2">
    Threats (Ancaman)</p></li>
<p style="line-height: 2;font-size: 12pt;font-family: 'times new roman';text-align: justify">
    Threats atau ancaman ini merupakan kebalikan dari peluang atau opportunities. Ancaman merupakan kondisi eksternal yang dapat mengganggu kelancaran berjalannya sebuah organisasi atau perusahaan. Ancaman dapat meliputi hal-hal dari lingkungan yang tidak menguntungkan bagi sebuah organisasi. Apabila ancaman tidak segera ditanggulangi maka dapat berakibat dampak berkepanjangan sehingga menjadi sebuah penghalang atau penghambattercapainya visi dan misi sebuah organisasi atau perusahaan. Ancaman bisa dilihat dari tingkat keparahan pengaruhnya (seriousness) dan kemungkinan terjadinya (probability of accurrence). Sama seperti peluan, ancaman juga dapat dikategorikan dalam tingkatan-tingkatan sebagai berikut :<br>
a. Ancaman utama (major threat) <br>
Sebuah ancaman yang kemungkinan terjadinya tinggi dan kemungkinan dapat berdampak besar. Menanggulangi ancaman jenis ini, maka diperlukan beberapa planning dan strategi yang serius agar ancaman ini tidak mengancam keberlangsungan hidup organisasi atau perusahaan <br>
b. Ancaman moderate (moderate threat) <br>
Jenis ancaman ini yang merupakan kombinasi tingkat keparahan dan kemungkinan terjado. Sebagai contoh ancaman jenis ini adalah kemungkinan tingkat keparahan yang tinggi namun kemungkinan terjadinya rendah, begitu sebaliknya. <br>
c. Ancaman tidak utama (minor threat) <br>
Ancaman ini merupakan jenis ancaman yang dampaknya kecil dan kemungkinan terjadinya juga kecil. Meskipun merupakan jenis ancaman yang ringan, namun sebaiknya jenis ancaman ini perlu segera dideteksi dan ditanggulangi. Hal tersebut tentu akan meminimalisasi kemungkinan ancaman tidak utama ini berubah menjadi ancaman yang lebih serius.
</p>
</ol>

