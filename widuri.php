b. Mengganti kerugian sebagai sebagai akibat dari kekeliruan/kelalaian dalam pelaksanaan tugasnya.<br>
c. Menjaga kas sekecil mungkin.<br>
d. Mengembalikan kepada lembaga setiap terjadi kelebihan transaksi.<br>
e. Memastikan ketersediaan form SUM/SUK dan segera berkoordinasi kepada Kepala Kantor bila persediaan hampir habis.</p>
