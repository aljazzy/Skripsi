c. Mengusahakan agar modal lembaga minimal 10% dari pasiva.<br>
d. Mengusahakan agar pinjaman beredar 70-80% dari asset dan pinjaman lalai berada dibawah 5% dari pinjaman beredar.<br>
e. Menjaga agar asset-asset yang tidak menghasilkan (biaya dibayar dimuka, aktiva tetap) selalu berada dibawah 5% dari total asset.<br>
f. Menjaga likuiditas agar selalu berada pada 10-20% dari asset.<br>
g. Membuka akses yang seluas-luasnya untuk audit/pengawasan.<br>
h. Memberikan laporan yang diperlukan oleh Manager.<br>
i. Menjaga dan memelihara semua kekayaan lembaga.
