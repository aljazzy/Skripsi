<div style="font-size: 14pt;font-family: 'times new roman';text-align: center"><p style="line-height: 2">ABSTRAKSI</p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 1"> Kemajuan teknologi informasi yang semakin canggih banyak dimanfaatkan dalam berbagai bidang khususnya pada perusahaan yang berbasis teknologi informasi. sistem teknologi informasi khususnya pada perusahaan yang berbasis IT tidak bisa sembarangan, melainkan harus dibuat dengan rapi supaya dapat berjalan optimal. Tujuan penelitian ialah menganalisa sistem yang sedang berjalan di PT.Booking Apartemen, serta merancang sistem reservasi yang handal untuk menjangkau pelanggan agar lebih mudah dalam pemesanan unit yang akan disewa. Metode analisis dilakukan dengan mempelajari pola customer, riset tentang masalah, dan wawancara kepada pihak pengelola sistem di perusahaan. Metode kepustakaan dilakukan dengan mencari informasi-informasi tambahan dari berbagai buku. Penulis mendapatkan hasil penelitian bahwa sistem manajemen sewa berbasis IT sangat efisien bagi perusahaan. Adapun manfaatnya adalah mempermudah pelanggan dalam memesan apartemen dan perusahaan dalam mengelola unit apartemen. Penerapan sistem aplikasi sewa apartemen pada perusahaan, menjadikan proses manajemen unit apartemen lebih baik dan sistematis</p></div>


<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 1">''Kata Kunci:  Analisis, Sewa Apartemen , Manajemen Sewa Apartemen .''</p></div>

<div style="font-size: 14pt;font-family: 'times new roman';text-align: center"><p style="line-height: 2">''ABSTRACT''</p></div>

<div style="font-size: 12pt;font-family: 'times new roman';text-align: justify"><p style="line-height: 1">''Advances in information technology are increasingly sophisticated widely used in various fields, especially in information technology-based companies. Information technology systems, especially in IT-based companies can not arbitrarily, but must be made with a neat order to run optimally. The purpose of the research was to analyze the system is running in PT.Booking Apartemen, as well as designing a reliable reservation system to make it easier to reach customers in ordering units to be rented. The analysis method to study the patterns of customers, research on the subject, and interview the manager of the system in the company. Library method is done by seeking additional information from various books. Writers get the results that IT-based rental management system is very efficient for the company. The benefit is easier for customers to order the company to manage apartments and apartment units. Implementation of the system of rental apartments in enterprise applications, making the process of better management of apartment units and systematic.'' </p></div>


